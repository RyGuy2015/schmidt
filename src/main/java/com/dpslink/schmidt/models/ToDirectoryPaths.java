package com.dpslink.schmidt.models;

public class ToDirectoryPaths {
	
	private String htmlDirectory;
	private String txtDirectory;
	private String imageDirectory;

	public String getHtmlDirectory() {
		return htmlDirectory;
	}
	public void setHtmlDirectory(String htmlDirectory) {
		this.htmlDirectory = htmlDirectory;
	}
	public String getTxtDirectory() {
		return txtDirectory;
	}
	public void setTxtDirectory(String txtDirectory) {
		this.txtDirectory = txtDirectory;
	}
	public String getImageDirectory() {
		return imageDirectory;
	}
	public void setImageDirectory(String imageDirectory) {
		this.imageDirectory = imageDirectory;
	}
	
}
